import numpy as np

a1 =np.arange(12, 24).reshape(3,4)

# Arithmetic operations on the array
print(a1)
print(a1 / 2)
print(a1 // 2)
print(a1 % 2)
print(a1 * 2)
print(a1 ** 2)
print(a1 + 2)
print(a1 - 2)

# Comparison operations on the array
print(a1)
print(a1 > 15)
print(a1 < 15)
print(a1 == 15)
print(a1 != 15)

